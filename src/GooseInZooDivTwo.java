	public static int count(String[] field, int dist)
	{
		vis = new boolean [field.length][field[0].length()];
		for(int i=0; i<field.length; i++)
			for(int j=0; j<field[i].length(); j++)
			{
				if(field[i].charAt(j)=='v') 
					{
					floodFill(field, i,j,dist,65);
					counter++;
					}
				
			}
		long prod = 1;
		for(int i=0; i<counter; i++)
			prod = prod*2 % mod;
		prod%=mod;
		prod-=1;
		prod+=mod;
		prod%=mod;
		if(counter == 0)  prod = 0;
		return (int)prod;
	}
	public static String replace(String s, int p, char c)
	{
		return s.substring(0,p)+c+s.substring(p+1);
	}
	public static void floodFill(String[] field, int a, int b, int depth, int ident)
	{
		//System.out.println(counter++);
		//System.out.println(depth+"d");
		field[a] = replace(field[a],b,'A');
		vis[a][b]=true;
		int i=0,j=0;
		for(i=a-depth; i<=a+depth; i++)
		{
			int o = depth-Math.abs(i-a);
			for(j = b-o; j<=b+o; j++)
				if(i>=0 && i<field.length && j>=0 && j<field[0].length() && field[i].charAt(j)=='v' && !vis[i][j]) 
				{
					//System.out.println(i+","+(j));
					vis[i][j]=true;
					floodFill(field, i, j, depth, ident);
				}


		}
	}
